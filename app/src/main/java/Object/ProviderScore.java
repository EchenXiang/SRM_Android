package Object;

public class ProviderScore {
    private String host_number;
    private String host;
    private String purchase_number;
    private String score;
    private String score_time;

    public String getHost_number() {
        return host_number;
    }

    public void setHost_number(String host_number) {
        this.host_number = host_number;
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public String getPurchase_number() {
        return purchase_number;
    }

    public void setPurchase_number(String purchase_number) {
        this.purchase_number = purchase_number;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }

    public String getScore_time() {
        return score_time;
    }

    public void setScore_time(String score_time) {
        this.score_time = score_time;
    }
}
